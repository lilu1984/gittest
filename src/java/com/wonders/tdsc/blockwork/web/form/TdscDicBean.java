package com.wonders.tdsc.blockwork.web.form;

public class TdscDicBean {
	private String dicCode;

	private String dicValue;

	private String dicNum;

	private String dicDescribe;

	private String isValidity;

	public String getDicCode() {
		return dicCode;
	}

	public void setDicCode(String dicCode) {
		this.dicCode = dicCode;
	}

	public String getDicDescribe() {
		return dicDescribe;
	}

	public void setDicDescribe(String dicDescribe) {
		this.dicDescribe = dicDescribe;
	}

	public String getDicNum() {
		return dicNum;
	}

	public void setDicNum(String dicNum) {
		this.dicNum = dicNum;
	}

	public String getDicValue() {
		return dicValue;
	}

	public void setDicValue(String dicValue) {
		this.dicValue = dicValue;
	}

	public String getIsValidity() {
		return isValidity;
	}

	public void setIsValidity(String isValidity) {
		this.isValidity = isValidity;
	}

}
